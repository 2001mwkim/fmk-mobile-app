class CircuitInfo {
  const CircuitInfo({
    this.lengthKm,
    this.turns,
    this.laps,
    this.distanceKm,
    this.firstYear,
    this.totalRacesHeld,
  });

  final double? lengthKm;
  final int? turns;
  final int? laps;
  final double? distanceKm;
  final int? firstYear;
  final int? totalRacesHeld;
}
